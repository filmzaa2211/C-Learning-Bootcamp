#include <stdio.h>

int main() {
    int x = 50;    // กล่อง x ใส่เลข 50
    int y = 10;    // กล่อง y ใส่เลข 10

    int *pX = &x;  // pX คือกุญแจ ชี้ไปที่กล่อง x
    int *pY = &y;  // pY คือกุญแจ ชี้ไปที่กล่อง y

    int sum;       // ตัวแปรไว้เก็บผลบวก
    int diff;      // ตัวแปรไว้เก็บผลลบ

    // เอา "ค่าที่ pX ชี้" + "ค่าที่ pY ชี้"
    // เหมือนหยิบเลขในกล่อง x กับ y มาบวกกัน
    sum = *pX + *pY;

    // เอา "ค่าที่ pX ชี้" - "ค่าที่ pY ชี้"
    // ห้ามใช้ x หรือ y ตรง ๆ
    diff = *pX - *pY;

    printf("Sum: %d\n", sum);        // แสดงผลบวก
    printf("Difference: %d\n", diff); // แสดงผลลบ

    return 0; // จบโปรแกรม
}