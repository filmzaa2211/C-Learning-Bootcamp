#include <stdio.h>

int main() {
    int a = 10;       // ตัวแปร a มีค่า 10 (เหมือนกล่องใส่เลข 10)
    int b = 20;       // ตัวแปร b มีค่า 20 (เหมือนกล่องใส่เลข 20)
    
    int *pA = &a;     // pA คือป้ายชื่อ ชี้ไปที่กล่อง a
    int *pB = &b;     // pB คือป้ายชื่อ ชี้ไปที่กล่อง b

    printf("--- Before Swap ---\n");
    printf("a = %d, b = %d\n", a, b);  // แสดงค่าก่อนสลับ

    // --- เริ่มสลับค่าของ a และ b ผ่าน pointer ---
    int temp = *pA;   // temp เก็บค่าของ a ไว้ก่อน (เก็บเลข 10 ชั่วคราว)
    *pA = *pB;        // เอาเลขของ b (20) ไปใส่ใน a
    *pB = temp;       // เอาเลขที่เก็บชั่วคราว (10) ไปใส่ใน b
    // --- สลับเสร็จแล้ว ---

    printf("--- After Swap ---\n");
    printf("a = %d, b = %d\n", a, b);  // แสดงค่าหลังสลับ

    return 0; // จบโปรแกรม
}
